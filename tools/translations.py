def build_ui_pairs(lang: str) -> list[tuple[str, str]]:
    """Build UI_ZH or UI_PT list from TRANSLATIONS, sorted by length (longest first)."""
    idx = 0 if lang == "zh" else 1
    pairs = [(en, t[idx]) for en, t in TRANSLATIONS]
    # Sort by English length descending to avoid partial replacements
    pairs.sort(key=lambda p: -len(p[0]))
    return pairs


def get_ui_zh() -> list[tuple[str, str]]:
    return build_ui_pairs("zh")


def get_ui_pt() -> list[tuple[str, str]]:
    return build_ui_pairs("pt")
